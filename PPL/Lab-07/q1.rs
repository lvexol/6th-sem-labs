fn main() {
    let integer: i32 = 42;          // Integer
    let float: f64 = 3.14;          // Floating-point
    let boolean: bool = true;       // Boolean
    let character: char = 'R';      // Character

    println!("Integer: {}", integer);
    println!("Float: {}", float);
    println!("Boolean: {}", boolean);
    println!("Character: {}", character);
}

